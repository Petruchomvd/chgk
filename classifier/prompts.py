def build_user_message(text: str, answer: str, comment: str = "") -> str:
    """Сформировать пользовательское сообщение для классификации."""
    parts = [f"Вопрос: {text}", f"Ответ: {answer}"]
    if comment:
        parts.append(f"Комментарий: {comment}")
    return "\n".join(parts)


def build_few_shot_messages() -> list:
    """Сформировать few-shot часть для промпта (формат Ollama chat API)."""
    import json

    messages = []
    for ex in FEW_SHOT_EXAMPLES:
        messages.append({
            "role": "user",
            "content": build_user_message(ex["question"], ex["answer"], ex.get("comment", "")),
        })
        messages.append({
            "role": "assistant",
            "content": json.dumps(ex["output"], ensure_ascii=False),
        })
    return messages


