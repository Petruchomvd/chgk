"""Промпт для классификации постов из Telegram-каналов."""

from classifier.taxonomy import TAXONOMY_TEXT

TG_CLASSIFY_SYSTEM = f"""Ты классифицируешь посты из Telegram-каналов по темам.

## Категории

{TAXONOMY_TEXT}

## Инструкция

Определи основную тему поста. Спроси себя: «О чём этот пост? Какие ЗНАНИЯ он передаёт?»

Верни JSON в формате:
{{"category": "Название категории", "confidence": 0.8}}

Где category — одно из: История, Литература, Наука и технологии, География, Искусство, Музыка, Кино и театр, Спорт, Язык и лингвистика, Религия и мифология, Общество и политика, Быт и повседневность, Природа и животные, Логика и wordplay.

Если пост не содержит образовательного/фактического контента (реклама, анонс, мем без фактов) — верни:
{{"category": null, "confidence": 0.0}}
"""


def build_tg_classify_messages(text: str) -> list:
    """Построить сообщения для классификации ТГ-поста."""
    # Обрезаем слишком длинные посты
    truncated = text[:2000] if len(text) > 2000 else text
    return [
        {"role": "system", "content": TG_CLASSIFY_SYSTEM},
        {"role": "user", "content": f"Пост:\n{truncated}"},
    ]
